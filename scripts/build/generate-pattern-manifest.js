import { mkdir, rm, writeFile } from "node:fs/promises";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";
import { validatePatternContracts } from "../contracts/pattern-contracts.js";

const ROOT = fileURLToPath(new URL("../..", import.meta.url));
const OUTPUT_DIR = join(ROOT, "generated/patterns");

function toPosixPath(path) {
  return path.replaceAll("\\", "/");
}

async function ensureDir(filePath) {
  await mkdir(dirname(filePath), { recursive: true });
}

function createManifest(entries) {
  const patterns = entries
    .map(({ contract, patternDir }) => ({
      name: contract.name,
      title: contract.title,
      slug: contract.slug,
      category: contract.category,
      family: contract.family,
      variant: contract.variant,
      scope: contract.scope,
      summary: contract.summary,
      tags: contract.tags,
      files: Object.fromEntries(
        Object.entries(contract.files).map(([key, value]) => [
          key,
          toPosixPath(relative(ROOT, join(patternDir, value))),
        ]),
      ),
      dependencies: {
        components: [...(contract.dependencies?.components ?? [])].sort(),
        patterns: [...(contract.dependencies?.patterns ?? [])].sort(),
      },
    }))
    .sort((a, b) => a.slug.localeCompare(b.slug));

  const categories = Object.fromEntries(
    [...new Set(patterns.map((pattern) => pattern.category))]
      .sort()
      .map((category) => [
        category,
        patterns.filter((pattern) => pattern.category === category).map((pattern) => pattern.slug),
      ]),
  );

  return {
    generatedAt: new Date().toISOString(),
    count: patterns.length,
    categories,
    patterns,
  };
}

function renderReadme(manifest) {
  const lines = [
    "# Dashbase Pattern Manifest",
    "",
    "This folder is generated by `scripts/build/generate-pattern-manifest.js`.",
    "",
    `Pattern count: ${manifest.count}`,
    "",
    "Categories:",
    "",
  ];

  for (const [category, slugs] of Object.entries(manifest.categories)) {
    lines.push(`- \`${category}\` (${slugs.length})`);
  }

  lines.push("");
  lines.push("Primary artifact:");
  lines.push("");
  lines.push("- `patterns.manifest.json`");
  lines.push("");

  return lines.join("\n");
}

export async function generatePatternManifest({ log = true } = {}) {
  const entries = await validatePatternContracts({ log: false });
  const manifest = createManifest(entries);

  await rm(OUTPUT_DIR, { recursive: true, force: true });
  await mkdir(OUTPUT_DIR, { recursive: true });

  const manifestPath = join(OUTPUT_DIR, "patterns.manifest.json");
  const readmePath = join(OUTPUT_DIR, "README.md");

  await ensureDir(manifestPath);
  await writeFile(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);
  await writeFile(readmePath, `${renderReadme(manifest)}\n`);

  if (log) {
    console.log(`Generated pattern manifest for ${manifest.count} patterns in ${toPosixPath(relative(ROOT, OUTPUT_DIR))}.`);
  }

  return manifest;
}

const isEntrypoint = process.argv[1] === fileURLToPath(import.meta.url);

if (isEntrypoint) {
  generatePatternManifest().catch((error) => {
    console.error(error);
    process.exit(1);
  });
}
